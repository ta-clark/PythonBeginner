# This program finds the difference of any three integers
print("Hello! Input any three numbers to find the difference.")

# Prompt user for any three numbers
num1= int(input("Enter first number: "))
num2= int(input("Enter second number: "))
num3= int(input("Enter third number: "))

# Compute the difference using PEMDAS
compute_all= num1 - num2 - num3

# Display the result
print("The difference of all three numbers is: ", compute_all)
